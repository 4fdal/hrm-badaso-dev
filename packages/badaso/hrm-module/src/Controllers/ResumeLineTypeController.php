    public function browse(Request $request)
    {
        try {

            $resume_line_types = new ResumeLineType();
            $resume_line_types_fillable = $resume_line_types->getFillable();

            $filter_fields = $request->get('filter_fields', '*');
            $filter_fields = explode(',', $filter_fields);
            if (array_search('*', $filter_fields) == false) {
                $new_filter_fields = [];
                foreach ($filter_fields as $index => $field) {
                    if (!in_array($field, $filter_fields)) {
                        $new_filter_fields[] = $field;
                    }
                }
                $resume_line_types = $resume_line_types->select($filter_fields);
            }

            $filter_fields_search = $request->get('filter_fields_search', '*');
            $filter_fields_search = explode(',', $filter_fields_search);
            if (isset($request->search)) {
                if (array_search('*', $filter_fields_search) == false) {
                    foreach ($resume_line_types_fillable as $index => $field) {
                        $resume_line_types = $resume_line_types->orWhere($field, "like", "%$request->search%");
                    }
                } else {
                    foreach ($filter_fields_search as $index => $field) {
                        if (in_array($field, $resume_line_types_fillable)) {
                            $resume_line_types = $resume_line_types->orWhere($field, "like", "%$request->search%");
                        }
                    }
                }
            }

            if ($request->get('show_pagination', 'true') == 'true') {
                $max_page = $request->get('max_page', 15);
                $resume_line_types = $resume_line_types->paginate($max_page);
            } else {
                $resume_line_types = $resume_line_types->get();
            }

            $resume_line_types->map(function($resume_line_types) use ($request){

            if($request->get("show_belogsto_relation", "false") == "true") {
                // belogs to relation
    
            }

            if($request->get("show_hasmany_relation", "false") == "true") {
                // has many relation
               $resume_line_types->resume_line_type_employee_resumes = $resume_line_types->resumeLineTypeEmployeeResumes ;
 
            }

                return $resume_line_types ;
            });
            $resume_line_types = $resume_line_types->toArray();

            return ApiResponse::success(compact('resume_line_types'));
        } catch (Exception $e) {
            return ApiResponse::failed($e);
        }
    }

