     *      ),
     *      @OA\Response(
     *          response=401,
     *          description="Unauthenticated",
     *      ),
     *      @OA\Response(
     *          response=403,
     *          description="Forbidden"
     *      ),
     *      security={{"bearerAuth" : {}}}
     * )
     */
    public function browse(Request $request)
    {
        try {

            $recruitment_sources = new RecruitmentSource();
            $recruitment_sources_fillable = $recruitment_sources->getFillable();

            $filter_fields = $request->get('filter_fields', '*');
            $filter_fields = explode(',', $filter_fields);
            if (array_search('*', $filter_fields) == false) {
                $new_filter_fields = [];
                foreach ($filter_fields as $index => $field) {
                    if (!in_array($field, $filter_fields)) {
                        $new_filter_fields[] = $field;
                    }
                }
                $recruitment_sources = $recruitment_sources->select($filter_fields);
            }

            $filter_fields_search = $request->get('filter_fields_search', '*');
            $filter_fields_search = explode(',', $filter_fields_search);
            if (isset($request->search)) {
                if (array_search('*', $filter_fields_search) == false) {
                    foreach ($recruitment_sources_fillable as $index => $field) {
                        $recruitment_sources = $recruitment_sources->orWhere($field, "like", "%$request->search%");
                    }
                } else {
                    foreach ($filter_fields_search as $index => $field) {
                        if (in_array($field, $recruitment_sources_fillable)) {
                            $recruitment_sources = $recruitment_sources->orWhere($field, "like", "%$request->search%");
                        }
                    }
                }
            }

            if ($request->get('show_pagination', 'true') == 'true') {
                $max_page = $request->get('max_page', 15);
                $recruitment_sources = $recruitment_sources->paginate($max_page);
            } else {
                $recruitment_sources = $recruitment_sources->get();
            }

            $recruitment_sources->map(function($recruitment_sources) use ($request){

            if($request->get("show_belogsto_relation", "false") == "true") {
                // belogs to relation
             $recruitment_sources->recruitment = $recruitment_sources->recruitment ;

            }

            if($request->get("show_hasmany_relation", "false") == "true") {
                // has many relation
    
            }

                return $recruitment_sources ;
            });
            $recruitment_sources = $recruitment_sources->toArray();

            return ApiResponse::success(compact('recruitment_sources'));
        } catch (Exception $e) {
            return ApiResponse::failed($e);
        }
    }

