     */
    public function browse(Request $request)
    {
        try {

            $recruitment_stages = new RecruitmentStage();
            $recruitment_stages_fillable = $recruitment_stages->getFillable();

            $filter_fields = $request->get('filter_fields', '*');
            $filter_fields = explode(',', $filter_fields);
            if (array_search('*', $filter_fields) == false) {
                $new_filter_fields = [];
                foreach ($filter_fields as $index => $field) {
                    if (!in_array($field, $filter_fields)) {
                        $new_filter_fields[] = $field;
                    }
                }
                $recruitment_stages = $recruitment_stages->select($filter_fields);
            }

            $filter_fields_search = $request->get('filter_fields_search', '*');
            $filter_fields_search = explode(',', $filter_fields_search);
            if (isset($request->search)) {
                if (array_search('*', $filter_fields_search) == false) {
                    foreach ($recruitment_stages_fillable as $index => $field) {
                        $recruitment_stages = $recruitment_stages->orWhere($field, "like", "%$request->search%");
                    }
                } else {
                    foreach ($filter_fields_search as $index => $field) {
                        if (in_array($field, $recruitment_stages_fillable)) {
                            $recruitment_stages = $recruitment_stages->orWhere($field, "like", "%$request->search%");
                        }
                    }
                }
            }

            if ($request->get('show_pagination', 'true') == 'true') {
                $max_page = $request->get('max_page', 15);
                $recruitment_stages = $recruitment_stages->paginate($max_page);
            } else {
                $recruitment_stages = $recruitment_stages->get();
            }

            $recruitment_stages->map(function($recruitment_stages) use ($request){

            if($request->get("show_belogsto_relation", "false") == "true") {
                // belogs to relation
    
            }

            if($request->get("show_hasmany_relation", "false") == "true") {
                // has many relation
    
            }

                return $recruitment_stages ;
            });
            $recruitment_stages = $recruitment_stages->toArray();

            return ApiResponse::success(compact('recruitment_stages'));
        } catch (Exception $e) {
            return ApiResponse::failed($e);
        }
    }

