     */
    public function browse(Request $request)
    {
        try {

            $calendar_alarams = new CalendarAlaram();
            $calendar_alarams_fillable = $calendar_alarams->getFillable();

            $filter_fields = $request->get('filter_fields', '*');
            $filter_fields = explode(',', $filter_fields);
            if (array_search('*', $filter_fields) == false) {
                $new_filter_fields = [];
                foreach ($filter_fields as $index => $field) {
                    if (!in_array($field, $filter_fields)) {
                        $new_filter_fields[] = $field;
                    }
                }
                $calendar_alarams = $calendar_alarams->select($filter_fields);
            }

            $filter_fields_search = $request->get('filter_fields_search', '*');
            $filter_fields_search = explode(',', $filter_fields_search);
            if (isset($request->search)) {
                if (array_search('*', $filter_fields_search) == false) {
                    foreach ($calendar_alarams_fillable as $index => $field) {
                        $calendar_alarams = $calendar_alarams->orWhere($field, "like", "%$request->search%");
                    }
                } else {
                    foreach ($filter_fields_search as $index => $field) {
                        if (in_array($field, $calendar_alarams_fillable)) {
                            $calendar_alarams = $calendar_alarams->orWhere($field, "like", "%$request->search%");
                        }
                    }
                }
            }

            if ($request->get('show_pagination', 'true') == 'true') {
                $max_page = $request->get('max_page', 15);
                $calendar_alarams = $calendar_alarams->paginate($max_page);
            } else {
                $calendar_alarams = $calendar_alarams->get();
            }

            $calendar_alarams->map(function($calendar_alarams) use ($request){

            if($request->get("show_belogsto_relation", "false") == "true") {
                // belogs to relation
    
            }

            if($request->get("show_hasmany_relation", "false") == "true") {
                // has many relation
               $calendar_alarams->calendar_alaram_calendar_reminders = $calendar_alarams->calendarAlaramCalendarReminders ;
 
            }

                return $calendar_alarams ;
            });
            $calendar_alarams = $calendar_alarams->toArray();

            return ApiResponse::success(compact('calendar_alarams'));
        } catch (Exception $e) {
            return ApiResponse::failed($e);
        }
    }

