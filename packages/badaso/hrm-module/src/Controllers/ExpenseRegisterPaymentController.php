    public function browse(Request $request)
    {
        try {

            $expense_register_payments = new ExpenseRegisterPayment();
            $expense_register_payments_fillable = $expense_register_payments->getFillable();

            $filter_fields = $request->get('filter_fields', '*');
            $filter_fields = explode(',', $filter_fields);
            if (array_search('*', $filter_fields) == false) {
                $new_filter_fields = [];
                foreach ($filter_fields as $index => $field) {
                    if (!in_array($field, $filter_fields)) {
                        $new_filter_fields[] = $field;
                    }
                }
                $expense_register_payments = $expense_register_payments->select($filter_fields);
            }

            $filter_fields_search = $request->get('filter_fields_search', '*');
            $filter_fields_search = explode(',', $filter_fields_search);
            if (isset($request->search)) {
                if (array_search('*', $filter_fields_search) == false) {
                    foreach ($expense_register_payments_fillable as $index => $field) {
                        $expense_register_payments = $expense_register_payments->orWhere($field, "like", "%$request->search%");
                    }
                } else {
                    foreach ($filter_fields_search as $index => $field) {
                        if (in_array($field, $expense_register_payments_fillable)) {
                            $expense_register_payments = $expense_register_payments->orWhere($field, "like", "%$request->search%");
                        }
                    }
                }
            }

            if ($request->get('show_pagination', 'true') == 'true') {
                $max_page = $request->get('max_page', 15);
                $expense_register_payments = $expense_register_payments->paginate($max_page);
            } else {
                $expense_register_payments = $expense_register_payments->get();
            }

            $expense_register_payments->map(function($expense_register_payments) use ($request){

            if($request->get("show_belogsto_relation", "false") == "true") {
                // belogs to relation
    
            }

            if($request->get("show_hasmany_relation", "false") == "true") {
                // has many relation
    
            }

                return $expense_register_payments ;
            });
            $expense_register_payments = $expense_register_payments->toArray();

            return ApiResponse::success(compact('expense_register_payments'));
        } catch (Exception $e) {
            return ApiResponse::failed($e);
        }
    }

