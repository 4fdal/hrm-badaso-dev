     *      security={{"bearerAuth" : {}}}
     * )
     */
    public function browse(Request $request)
    {
        try {

            $skill_types = new SkillType();
            $skill_types_fillable = $skill_types->getFillable();

            $filter_fields = $request->get('filter_fields', '*');
            $filter_fields = explode(',', $filter_fields);
            if (array_search('*', $filter_fields) == false) {
                $new_filter_fields = [];
                foreach ($filter_fields as $index => $field) {
                    if (!in_array($field, $filter_fields)) {
                        $new_filter_fields[] = $field;
                    }
                }
                $skill_types = $skill_types->select($filter_fields);
            }

            $filter_fields_search = $request->get('filter_fields_search', '*');
            $filter_fields_search = explode(',', $filter_fields_search);
            if (isset($request->search)) {
                if (array_search('*', $filter_fields_search) == false) {
                    foreach ($skill_types_fillable as $index => $field) {
                        $skill_types = $skill_types->orWhere($field, "like", "%$request->search%");
                    }
                } else {
                    foreach ($filter_fields_search as $index => $field) {
                        if (in_array($field, $skill_types_fillable)) {
                            $skill_types = $skill_types->orWhere($field, "like", "%$request->search%");
                        }
                    }
                }
            }

            if ($request->get('show_pagination', 'true') == 'true') {
                $max_page = $request->get('max_page', 15);
                $skill_types = $skill_types->paginate($max_page);
            } else {
                $skill_types = $skill_types->get();
            }

            $skill_types->map(function($skill_types) use ($request){

            if($request->get("show_belogsto_relation", "false") == "true") {
                // belogs to relation
    
            }

            if($request->get("show_hasmany_relation", "false") == "true") {
                // has many relation
               $skill_types->skill_type_skill_levels = $skill_types->skillTypeSkillLevels ;
            $skill_types->skill_type_skills = $skill_types->skillTypeSkills ;
            $skill_types->skill_type_employee_skills = $skill_types->skillTypeEmployeeSkills ;
 
            }

                return $skill_types ;
            });
            $skill_types = $skill_types->toArray();

            return ApiResponse::success(compact('skill_types'));
        } catch (Exception $e) {
            return ApiResponse::failed($e);
        }
    }

