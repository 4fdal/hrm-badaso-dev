     */
    public function browse(Request $request)
    {
        try {

            $partner_titles = new PartnerTitle();
            $partner_titles_fillable = $partner_titles->getFillable();

            $filter_fields = $request->get('filter_fields', '*');
            $filter_fields = explode(',', $filter_fields);
            if (array_search('*', $filter_fields) == false) {
                $new_filter_fields = [];
                foreach ($filter_fields as $index => $field) {
                    if (!in_array($field, $filter_fields)) {
                        $new_filter_fields[] = $field;
                    }
                }
                $partner_titles = $partner_titles->select($filter_fields);
            }

            $filter_fields_search = $request->get('filter_fields_search', '*');
            $filter_fields_search = explode(',', $filter_fields_search);
            if (isset($request->search)) {
                if (array_search('*', $filter_fields_search) == false) {
                    foreach ($partner_titles_fillable as $index => $field) {
                        $partner_titles = $partner_titles->orWhere($field, "like", "%$request->search%");
                    }
                } else {
                    foreach ($filter_fields_search as $index => $field) {
                        if (in_array($field, $partner_titles_fillable)) {
                            $partner_titles = $partner_titles->orWhere($field, "like", "%$request->search%");
                        }
                    }
                }
            }

            if ($request->get('show_pagination', 'true') == 'true') {
                $max_page = $request->get('max_page', 15);
                $partner_titles = $partner_titles->paginate($max_page);
            } else {
                $partner_titles = $partner_titles->get();
            }

            $partner_titles->map(function($partner_titles) use ($request){

            if($request->get("show_belogsto_relation", "false") == "true") {
                // belogs to relation
    
            }

            if($request->get("show_hasmany_relation", "false") == "true") {
                // has many relation
               $partner_titles->partner_title_company_contacts = $partner_titles->partnerTitleCompanyContacts ;
 
            }

                return $partner_titles ;
            });
            $partner_titles = $partner_titles->toArray();

            return ApiResponse::success(compact('partner_titles'));
        } catch (Exception $e) {
            return ApiResponse::failed($e);
        }
    }

