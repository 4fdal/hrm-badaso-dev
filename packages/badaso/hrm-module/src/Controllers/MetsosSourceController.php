     *      ),
     *      security={{"bearerAuth" : {}}}
     * )
     */
    public function browse(Request $request)
    {
        try {

            $metsos_sources = new MetsosSource();
            $metsos_sources_fillable = $metsos_sources->getFillable();

            $filter_fields = $request->get('filter_fields', '*');
            $filter_fields = explode(',', $filter_fields);
            if (array_search('*', $filter_fields) == false) {
                $new_filter_fields = [];
                foreach ($filter_fields as $index => $field) {
                    if (!in_array($field, $filter_fields)) {
                        $new_filter_fields[] = $field;
                    }
                }
                $metsos_sources = $metsos_sources->select($filter_fields);
            }

            $filter_fields_search = $request->get('filter_fields_search', '*');
            $filter_fields_search = explode(',', $filter_fields_search);
            if (isset($request->search)) {
                if (array_search('*', $filter_fields_search) == false) {
                    foreach ($metsos_sources_fillable as $index => $field) {
                        $metsos_sources = $metsos_sources->orWhere($field, "like", "%$request->search%");
                    }
                } else {
                    foreach ($filter_fields_search as $index => $field) {
                        if (in_array($field, $metsos_sources_fillable)) {
                            $metsos_sources = $metsos_sources->orWhere($field, "like", "%$request->search%");
                        }
                    }
                }
            }

            if ($request->get('show_pagination', 'true') == 'true') {
                $max_page = $request->get('max_page', 15);
                $metsos_sources = $metsos_sources->paginate($max_page);
            } else {
                $metsos_sources = $metsos_sources->get();
            }

            $metsos_sources->map(function($metsos_sources) use ($request){

            if($request->get("show_belogsto_relation", "false") == "true") {
                // belogs to relation
    
            }

            if($request->get("show_hasmany_relation", "false") == "true") {
                // has many relation
               $metsos_sources->metsos_source_applicants = $metsos_sources->metsosSourceApplicants ;
 
            }

                return $metsos_sources ;
            });
            $metsos_sources = $metsos_sources->toArray();

            return ApiResponse::success(compact('metsos_sources'));
        } catch (Exception $e) {
            return ApiResponse::failed($e);
        }
    }

