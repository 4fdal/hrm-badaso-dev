     *          response=401,
     *          description="Unauthenticated",
     *      ),
     *      @OA\Response(
     *          response=403,
     *          description="Forbidden"
     *      ),
     *      security={{"bearerAuth" : {}}}
     * )
     */
    public function browse(Request $request)
    {
        try {

            $degrees = new Degree();
            $degrees_fillable = $degrees->getFillable();

            $filter_fields = $request->get('filter_fields', '*');
            $filter_fields = explode(',', $filter_fields);
            if (array_search('*', $filter_fields) == false) {
                $new_filter_fields = [];
                foreach ($filter_fields as $index => $field) {
                    if (!in_array($field, $filter_fields)) {
                        $new_filter_fields[] = $field;
                    }
                }
                $degrees = $degrees->select($filter_fields);
            }

            $filter_fields_search = $request->get('filter_fields_search', '*');
            $filter_fields_search = explode(',', $filter_fields_search);
            if (isset($request->search)) {
                if (array_search('*', $filter_fields_search) == false) {
                    foreach ($degrees_fillable as $index => $field) {
                        $degrees = $degrees->orWhere($field, "like", "%$request->search%");
                    }
                } else {
                    foreach ($filter_fields_search as $index => $field) {
                        if (in_array($field, $degrees_fillable)) {
                            $degrees = $degrees->orWhere($field, "like", "%$request->search%");
                        }
                    }
                }
            }

            if ($request->get('show_pagination', 'true') == 'true') {
                $max_page = $request->get('max_page', 15);
                $degrees = $degrees->paginate($max_page);
            } else {
                $degrees = $degrees->get();
            }

            $degrees->map(function($degrees) use ($request){

            if($request->get("show_belogsto_relation", "false") == "true") {
                // belogs to relation
    
            }

            if($request->get("show_hasmany_relation", "false") == "true") {
                // has many relation
               $degrees->certificate_level_employees = $degrees->certificateLevelEmployees ;
            $degrees->degree_applicants = $degrees->degreeApplicants ;
 
            }

                return $degrees ;
            });
            $degrees = $degrees->toArray();

            return ApiResponse::success(compact('degrees'));
        } catch (Exception $e) {
            return ApiResponse::failed($e);
        }
    }

